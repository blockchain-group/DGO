%   f = -186.730908831024
%   x = (-7.08350658,  4.85805691), ( 5.48286415,  4.85805681),
%       ( 4.85805691, -7.08350658), ( 4.85805691,  5.48286415),
%       (-7.08350658, -7.70831382), (-7.70831382, -7.08350658),
